package practice3.model;

public class Member {

	int mno;
	String id;
	String pw;
	String name;
	String phone;
	int height;
	int weight;
	
	public Member() {	}

	public Member(int mno, String id, String pw, String name, String phone, int height, int weight) {
		this.mno = mno;
		this.id = id;
		this.pw = pw;
		this.name = name;
		this.phone = phone;
		this.height = height;
		this.weight = weight;
	}
	
	
	
}
